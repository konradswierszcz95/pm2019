package pl.kspm.hello.form;

public class AddEmployeeForm {
    private String employeeName;
    private String employeeSurname;
    private String employeeEmail;
    private String employeeLocality;
    private String employeeStreet;
    private String employeeZipCode;
    private String employeePostLocality;
    private String employeeHouseNumber;
    private String employeeCountry;
    private String employeePhone;

    public AddEmployeeForm() {
        super();
    }

    public String getEmployeeName() {
        return employeeName;
    }

    public void setEmployeeName(String employeeName) {
        this.employeeName = employeeName;
    }

    public String getEmployeeSurname() {
        return employeeSurname;
    }

    public void setEmployeeSurname(String employeeSurname) {
        this.employeeSurname = employeeSurname;
    }

    public String getEmployeeEmail() {
        return employeeEmail;
    }

    public void setEmployeeEmail(String employeeEmail) {
        this.employeeEmail = employeeEmail;
    }

    public String getEmployeeLocality() {
        return employeeLocality;
    }

    public void setEmployeeLocality(String employeeLocality) {
        this.employeeLocality = employeeLocality;
    }

    public String getEmployeeStreet() {
        return employeeStreet;
    }

    public void setEmployeeStreet(String employeeStreet) {
        this.employeeStreet = employeeStreet;
    }

    public String getEmployeeZipCode() {
        return employeeZipCode;
    }

    public void setEmployeeZipCode(String employeeZipCode) {
        this.employeeZipCode = employeeZipCode;
    }

    public String getEmployeePostLocality() {
        return employeePostLocality;
    }

    public void setEmployeePostLocality(String employeePostLocality) {
        this.employeePostLocality = employeePostLocality;
    }

    public String getEmployeeHouseNumber() {
        return employeeHouseNumber;
    }

    public void setEmployeeHouseNumber(String employeeHouseNumber) {
        this.employeeHouseNumber = employeeHouseNumber;
    }

    public String getEmployeeCountry() {
        return employeeCountry;
    }

    public void setEmployeeCountry(String employeeCountry) {
        this.employeeCountry = employeeCountry;
    }

    public String getEmployeePhone() {
        return employeePhone;
    }

    public void setEmployeePhone(String employeePhone) {
        this.employeePhone = employeePhone;
    }
}
